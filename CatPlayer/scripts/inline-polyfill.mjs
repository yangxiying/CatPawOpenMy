#!/usr/bin/env node
/**
 * inline-polyfill — 将 polyfills.js 中的 POLYFILL_SOURCE() 转为字符串常量模块。
 *
 * 解决的问题：Hermes（RN 0.74+ iOS 默认 JS 引擎）的 Function.prototype.toString()
 * 从字节码反编译源码时可能产生畸形代码（含 bytecode 变量引用），导致 WebView 执行失败。
 *
 * 此脚本在构建前运行：读取 polyfills.js → 提取函数体 → 生成 polyfill-string.ts（纯字符串导出）。
 * WebViewNode 导入该字符串，避免 .toString() 调用。
 */

import fs from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const SRC = join(root, 'overlay/src/node/polyfills.js');
const DST = join(root, 'overlay/src/node/polyfill-string.ts');

const content = fs.readFileSync(SRC, 'utf8');

// 定位 POLYFILL_SOURCE() 函数
const fnStart = content.indexOf('function POLYFILL_SOURCE()');
if (fnStart === -1) {
    console.error('ERROR: cannot find POLYFILL_SOURCE() function in polyfills.js');
    process.exit(1);
}

// 找到函数体的开始（第一个 {）
const braceStart = content.indexOf('{', fnStart);
if (braceStart === -1) {
    console.error('ERROR: cannot find opening brace in POLYFILL_SOURCE()');
    process.exit(1);
}

// 匹配嵌套 {} 找到闭合位置
let depth = 1;
let pos = braceStart + 1;
while (depth > 0 && pos < content.length) {
    const ch = content[pos];
    if (ch === '{') depth++;
    else if (ch === '}') depth--;
    pos++;
}
if (depth !== 0) {
    console.error('ERROR: unmatched braces in POLYFILL_SOURCE()');
    process.exit(1);
}

const fnSource = content.slice(fnStart, pos);
const escaped = fnSource.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const tsCode = `// AUTO-GENERATED by scripts/inline-polyfill.mjs -- DO NOT EDIT
// Source: overlay/src/node/polyfills.js (POLYFILL_SOURCE function)
// Regenerate: node scripts/inline-polyfill.mjs

export const polyfillCode = \`${escaped}\`;
`;

fs.writeFileSync(DST, tsCode, 'utf8');
console.log(`polyfill-string.ts generated (${fnSource.length} bytes -> ${tsCode.length} bytes)`);
